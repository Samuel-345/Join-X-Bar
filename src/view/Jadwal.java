    private void loadTableData(){
        DefaultTableModel dtm = (DefaultTableModel) roleTable.getModel();
        refreshTable(dtm);
    }  
    
    private void refreshTable(DefaultTableModel dtm) {
        // Refresh tabel
            while (dtm.getRowCount() > 0) {
                dtm.removeRow(0);
            }

            try (Connection conn = Database.connect()) {
                String sql = "SELECT role, ruangan, waktu FROM jadwal";
                try (PreparedStatement stmt = conn.prepareStatement(sql);
                     ResultSet rs = stmt.executeQuery()) {

                    // Isi tabel
                    while (rs.next()) {
                        String role = rs.getString("role");
                        String ruangan = rs.getString("ruangan");
                        String waktu = rs.getString("waktu");
                        dtm.addRow(new Object[]{role, ruangan, waktu});
                    }
                }
            } catch (SQLException ex) {
                System.err.println(ex);
                JOptionPane.showMessageDialog(this, "Gagal mengambil data", "ERROR", JOptionPane.ERROR_MESSAGE);
            }
    }

