    private void buttonLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonLoginActionPerformed
        // TODO add your handling code here:
 
        if (fieldPassword.getText().equals("") || fieldUsername.getText().equals("") ) {
            JOptionPane.showMessageDialog(null, "Username dan Password harus diisi!","WARNING",JOptionPane.WARNING_MESSAGE);
        } else if ( fieldUsername.getText().equals("Admin") && fieldPassword.getText().equals("admin123") ) {
            JOptionPane.showMessageDialog(null,"Berhasil login sebagai admin!","LOGIN SUCCESS",JOptionPane.INFORMATION_MESSAGE);
            // menuju ke page admin
            Admin adm = new Admin();
            adm.setVisible(true);
            adm.pack();
            adm.setLocationRelativeTo(null);
            adm.setDefaultCloseOperation(Form.EXIT_ON_CLOSE);
            this.dispose();
        }
        else {
            username = fieldUsername.getText();
            password = fieldPassword.getText();

            try {
                Connection conn = Database.connect();
                
                String sql = "SELECT id FROM akun WHERE username = ? AND password = ?";
                PreparedStatement pstmt = conn.prepareStatement(sql);
                pstmt.setString(1, username);
                pstmt.setString(2, password);
                ResultSet rs = pstmt.executeQuery();

                if (rs.next()) {
                    int id = rs.getInt("id");
                    // Check apakah nilai id ada di tabel member
                    sql = "SELECT nim, nama, kelas, role FROM member WHERE id = ?";
                    PreparedStatement stmt = conn.prepareStatement(sql);
                    stmt.setInt(1, id);
                    ResultSet rsMember = stmt.executeQuery();
                    
                    if (rsMember.next()) {
                        String nim = rsMember.getString("nim");
                        String nama = rsMember.getString("nama");
                        String kelas = rsMember.getString("kelas");
                        String role = rsMember.getString("role");
                        JOptionPane.showMessageDialog(null,"Berhasil login!","LOGIN SUCCESS",JOptionPane.INFORMATION_MESSAGE);

                        // menuju ke home2
                        Home2 hm = new Home2(role, nim, nama, kelas);
                        hm.setVisible(true);
                        hm.pack();
                        hm.setLocationRelativeTo(null);
                        hm.setDefaultCloseOperation(Form.EXIT_ON_CLOSE);
                        this.dispose();
                    } else {
                        
                        JOptionPane.showMessageDialog(null,"Berhasil login!","LOGIN SUCCESS",JOptionPane.INFORMATION_MESSAGE);
                        // menuju ke home
                        Home hm = new Home();
                        hm.setVisible(true);
                        hm.pack();
                        hm.setLocationRelativeTo(null);
                        hm.setDefaultCloseOperation(Form.EXIT_ON_CLOSE);
                        this.dispose();
                    }
                    rsMember.close();
                    stmt.close();
                }   
                   else {
                    JOptionPane.showMessageDialog(null, "Username atau password tidak valid!", "LOGIN FAILED", JOptionPane.ERROR_MESSAGE);
                }

                rs.close();
                pstmt.close();
                conn.close();

            } catch (SQLException e) {
                JOptionPane.showMessageDialog(this, "Database error: " + e.getMessage(), "ERROR", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_buttonLoginActionPerformed

