    private void fieldUsernameKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_fieldUsernameKeyTyped
        // TODO add your handling code here:
    // Mendapatkan karakter yang diketik
    char c = evt.getKeyChar();

    // Memeriksa apakah panjang teks melebihi 20 atau karakter bukan huruf/angka
    if (fieldUsername.getText().length() >= 20 || !Character.isLetterOrDigit(c)) {
        evt.consume(); // Membatalkan aksi jika tidak sesuai kriteria
    }
    }//GEN-LAST:event_fieldUsernameKeyTyped

